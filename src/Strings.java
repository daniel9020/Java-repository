public class Strings {

    public static void main(String [] args) {
        int x = 3;
        String s = " Hello, ";
        String w = "World!";
        System.out.println(x + s + w);
        System.out.println("Hello, World!");
        String exa = "Exa";
        String mple = "mple";
        System.out.println(exa + mple);
        System.out.println("I'm " + x);
    }
}
