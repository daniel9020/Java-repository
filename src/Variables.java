public class Variables {

    public static void main(String [] args) {

        int myInt;
        myInt = 1507;
        // short - числа от -32000 до 32000
        short myShort = 32000;
        //long - большие числа
        long myLong = 562498249;

        double myDouble = 1.21;
        float myFloat = 1.5412F;

        char myChar = 'a';
        boolean myBoolean = true;

        // -128 до 128
        byte myByte = 41;
        System.out.println(myInt);
    }
}
